<?php

namespace Junisan\ListmonkApi\Models\Utils;

trait IdModelTrait
{
    private ?int $id;
    private ?string $uuid;

    public function getId(): ?int
    {
        return $this->id;
    }

    public function setId(?int $id): self
    {
        $this->id = $id;
        return $this;
    }

    public function getUuid(): ?string
    {
        return $this->uuid;
    }

    public function setUuid(?string $uuid): self
    {
        $this->uuid = $uuid;
        return $this;
    }
}